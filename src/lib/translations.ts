export type TranslationKey = string;

export function formatTranslation(template: string, values?: Record<string, string | number>) {
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? `{${key}}`));
}

export function getTranslation(
  language: keyof typeof translations,
  key: TranslationKey,
  values?: Record<string, string | number>
) {
  const current = getTranslationValue(translations[language], key);
  const fallback = getTranslationValue(translations.en, key);
  if (typeof current !== 'string') {
    console.warn(`[i18n] Missing translation for key: ${key} in language: ${language}`);
  }
  const value = typeof current === 'string' ? current : typeof fallback === 'string' ? fallback : key;
  return formatTranslation(value, values);
}

function getTranslationValue(tree: TranslationTree, key: string): TranslationValue | undefined {
  return key.split('.').reduce<TranslationValue | undefined>((value, part) => {
    if (!value || typeof value === 'string') return undefined;
    return value[part];
  }, tree);
}

